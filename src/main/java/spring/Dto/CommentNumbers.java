package spring.Dto;


public class CommentNumbers {

	private int commentStartRow;
	private int commentEndRow;
	private int content_num;
	
	
	
	public int getContent_num() {
		return content_num;
	}
	public void setContent_num(int content_num) {
		this.content_num = content_num;
	}
	public int getCommentStartRow() {
		return commentStartRow;
	}
	public void setCommentStartRow(int commentStartRow) {
		this.commentStartRow = commentStartRow;
	}
	public int getCommentEndRow() {
		return commentEndRow;
	}
	public void setCommentEndRow(int commentEndRow) {
		this.commentEndRow = commentEndRow;
	}
	
	
	
}
